//  used to store and manipulate the data
//  also called as named containers 
//  var, let, const

// var --- values and variables can be redefined 

var a = "soumya";
console.log(a);

var a = "sony";
console.log(a);

// let --- cannot be redefined

let x = 100;
console.log(x);

x = 10;
console.log(x);

// neither the value or variables cannot be redefined

const e = 20;
console.log(e);

